package net.codejava;
// Declare and initalize all data types variable and print the same
public class Variables {

	public static void main(String[] args) {
		//boolean
		boolean bool = true;
		System.out.println("Boolean \t" + bool);
		
		//byte
		byte by = 5;
		System.out.println("Byte \t" + by);
		
		// short
		short num = 6366;
		System.out.println("Short \t" + num);
		
		//int 
		int number = 6999999;
		System.out.println("number \t" +number);
		
		//long 
		long num2 = 96666666666l;
		System.out.println("Long \t" + num2);
		
		//float
		float num3 = 69.3666666f;
		System.out.println("Float \t" + num3);
		
		//double
		double num4 = 6987.64845676467;
		System.out.println("Double \t" + num4);
		
		//char with unicode
		char ch = '\u0041';
		System.out.println("char unicode \t "+ ch);
		
		//char with ASCII
		char ch2 = 65;
		System.out.println("char ascii \t " +ch2);
		
		//char 
		char ch3 = 'A';
		System.out.println("char \t " +ch3);
		
		
		
		
		

	}

}
